package org.taktik.icure.dao.impl.ektorp;

import org.ektorp.ComplexKey;

public class CouchKeyValue<T> {
	ComplexKey key;
	String id;
	T value;

	public CouchKeyValue(String id, ComplexKey key, T value) {
		this.id = id;
		this.key = key;
		this.value = value;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public ComplexKey getKey() {
		return key;
	}

	public void setKey(ComplexKey key) {
		this.key = key;
	}

	public T getValue() {
		return value;
	}

	public void setValue(T value) {
		this.value = value;
	}
}
